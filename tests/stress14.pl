% Arithmetic stress test for N-Prolog

show(Test, Goal) :-
    write(Test),
    write(' : '),
    call(Goal),
    nl.

run_all :-
    show(test1, test1),
    show(test2, test2),
    show(test3, test3),
    show(test4, test4),
    show(test5, test5),
    show(test6, test6),
    show(test7, test7),
    show(test8, test8),
    show(test9, test9),
    show(test10, test10),
    show(test11, test11),
    show(test12, test12),
    show(test13, test13),
    show(test14, test14),
    show(test15, test15),
    show(test16, test16),
