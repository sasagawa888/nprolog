%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 1. 優先順位（最重要）
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

% * が + より強い
test1 :-
    X is 1 + 2 * 3,
    write(X).      % 7

% 左結合
test2 :-
    X is 10 - 3 - 2,
    write(X).      % 5

% カッコ優先
test3 :-
    X is (10 - 3) - 2,
    write(X).      % 5

test4 :-
    X is 10 - (3 - 2),
    write(X).      % 9

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 2. 混合演算
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test5 :-
    X is 2 + 3 * 4 - 5,
    write(X).      % 9

test6 :-
    X is 2 * (3 + 4) - 5,
    write(X).      % 9

test7 :-
    X is 20 / 5 + 3,
    write(X).      % 7.0 or 7

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 3. 単項演算
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test8 :-
    X is -5 + 3,
    write(X).      % -2

test9 :-
    X is -(5 + 3),
    write(X).      % -8

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 4. 変数を含む
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test10 :-
    A = 3,
    B = 4,
    X is A * B + 2,
    write(X).      % 14

test11 :-
    A = 10,
    B = 2,
    X is A / B,
    write(X).      % 5.0 or 5

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 5. ネスト（パーサ壊れやすい）
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test12 :-
    X is (1 + (2 * (3 + (4 * 5)))),
    write(X).      % 1 + 2*(3+20)=1+2*23=47

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 6. 比較との組み合わせ
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test13 :-
    X is 3 * 3,
    ( X > 5 -> write(ok) ; write(ng) ).   % ok

test14 :-
    X is 2 + 2,
    ( X =:= 4 -> write(eq) ; write(ne) ). % eq

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 7. 再帰と算術（TCOとの相互作用）
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

sum_to(0, A, A).
sum_to(N, A, X) :-
    N > 0,
    B is A + N,
    M is N - 1,
    sum_to(M, B, X).

test15 :-
    sum_to(5, 0, X),
    write(X).      % 15

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 8. div / mod 系（実装差出やすい）
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test16 :-
    X is 7 // 2,
    write(X).      % 3

test17 :-
    X is 7 mod 2,
    write(X).      % 1

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 9. 境界（0, 負数）
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test18 :-
    X is 0 * 100 + 5,
    write(X).      % 5

test19 :-
    X is -10 // 3,
    write(X).      % 実装依存（-3 or -4）

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 10. ちょっと意地悪
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test20 :-
    A = 2,
    X is A * A + A * (A + 1),
    write(X).      % 4 + 2*3 = 10