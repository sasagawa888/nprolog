show(Name, X) :-
    write(Name), write(' : '), write(X), nl.

show2(Name, X, Y) :-
    write(Name), write(' : '), write(X), write(','), write(Y), nl.

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 1. basic addition
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p1(X) :-
    X is 1 + 2.

test1 :-
    p1(X),
    show(test1, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 2. precedence: multiplication before addition
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p2(X) :-
    X is 1 + 2 * 3.

test2 :-
    p2(X),
    show(test2, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 3. parentheses
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p3(X) :-
    X is (1 + 2) * 3.

test3 :-
    p3(X),
    show(test3, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 4. chained temporary variables
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p4(X) :-
    A is 10 + 1,
    B is A * 2,
    X is B - 5.

test4 :-
    p4(X),
    show(test4, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 5. variable reuse in arithmetic chain
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p5(N, X) :-
    A is N + 1,
    B is A + 2,
    C is B + 3,
    X is C + 4.

test5 :-
    p5(0, X),
    show(test5, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 6. unary minus
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p6(X) :-
    X is -(3 + 2).

test6 :-
    p6(X),
    show(test6, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 7. double unary style
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p7(X) :-
    A is -5,
    X is A + 8.

test7 :-
    p7(X),
    show(test7, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 8. division-like operators
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p8(X, Y) :-
    X is 17 // 5, % div in ISO
    Y is 17 mod 5.

test8 :-
    p8(X, Y),
    show2(test8, X, Y).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 9. comparison after arithmetic
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p9(N, X) :-
    A is N * 2,
    ( A > 10 ->
        X = big
    ;
        X = small
    ).

test9 :-
    p9(4, X),
    show(test9, X).

