%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 20. arithmetic structure generation
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p20(N, X) :-
    A is N + 2,
    B is A * 3,
    X = result(A, B).

test20 :-
    p20(4, X),
    show(test20, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 21. deeper expression tree
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p21(X) :-
    X is ((2 + 3) * (4 + 1)) - (6 // 2). % div in ISO

test21 :-
    p21(X),
    show(test21, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 22. arithmetic in multi-step else tail
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p22(N, X) :-
    ( N > 100 ->
        X is N * 10
    ;
        A is N + 1,
        B is A * 2,
        C is B + 3,
        X is C - 4
    ).

test22 :-
    p22(5, X),
    show(test22, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 23. branch then recursive tail
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p23(0, X, X).
p23(N, X, Y) :-
    N > 0,
    ( X < 10 ->
        X1 is X + 2
    ;
        X1 is X * 2
    ),
    N1 is N - 1,
    p23(N1, X1, Y).

test23 :-
    p23(4, 1, X),
    show(test23, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 24. arithmetic + exact compare in recursion
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p24(0, Acc, Acc).
p24(N, Acc, Res) :-
    N > 0,
    A is N mod 2,
    ( A =:= 0 ->
        Acc1 is Acc + 100
    ;
        Acc1 is Acc + 1
    ),
    N1 is N - 1,
    p24(N1, Acc1, Res).

test24 :-
    p24(5, 0, X),
    show(test24, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 25. arithmetic and fail/success wrapper
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p25(N, X) :-
    A is N + 1,
    A > 0,
    X is A * 2.

test25 :-
    ( p25(3, X) ->
        show(test25, X)
    ;
        show(test25, fail)
    ).
    
