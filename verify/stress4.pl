%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 11. else branch arithmetic
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p11(N, X) :-
    ( N > 0 ->
        X = ok
    ;
        A is N - 1,
        B is A - 2,
        X is B * 3
    ).

test11 :-
    p11(0, X),
    show(test11, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 12. nested arithmetic branches
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p12(N, X) :-
    ( N > 0 ->
        ( N > 10 ->
            X is N * 2
        ;
            X is N + 2
        )
    ;
        X is N - 2
    ).

test12 :-
    p12(5, X),
    show(test12, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 13. =:= exact arithmetic comparison
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p13(A, B, X) :-
    C is A + B,
    ( C =:= 10 ->
        X = yes
    ;
        X = no
    ).

test13 :-
    p13(4, 6, X),
    show(test13, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 14. =\= arithmetic inequality
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p14(A, B, X) :-
    C is A * B,
    ( C =\= 12 ->
        X = diff
    ;
        X = same
    ).

test14 :-
    p14(3, 4, X),
    show(test14, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 15. < and >= mixed
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p15(N, X) :-
    A is N + 3,
    ( A < 5 ->
        X = low
    ; A >= 5 ->
        X = high
    ;
        X = impossible
    ).

test15 :-
    p15(3, X),
    show(test15, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 16. recursive accumulation
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p16(0, Acc, Acc).
p16(N, Acc, Res) :-
    N > 0,
    Acc1 is Acc + N,
    N1 is N - 1,
    p16(N1, Acc1, Res).

test16 :-
    p16(4, 0, X),
    show(test16, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 17. recursive arithmetic with branch
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p17(0, X, X).
p17(N, X, Y) :-
    N > 0,
    ( N mod 2 =:= 0 ->
        X1 is X + 10
    ;
        X1 is X + 1
    ),
    N1 is N - 1,
    p17(N1, X1, Y).

test17 :-
    p17(4, 0, X),
    show(test17, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 18. arithmetic using previous temp result
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p18(A, B, X) :-
    C is A + B,
    D is C * C,
    E is D - C,
    X is E + 1.

test18 :-
    p18(2, 3, X),
    show(test18, X).

