show(Name, X) :-
    write(Name), write(' : '), write(X), nl.

show2(Name, X, Y) :-
    write(Name), write(' : '), write(X), write(','), write(Y), nl.

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 1. then 節で束縛した変数が正しく返るか
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p1(N, X) :-
    ( N > 0 ->
        X = positive
    ;
        X = nonpositive
    ).

test1 :-
    p1(3, X),
    show(test1, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 2. else 節で束縛した変数が正しく返るか
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p2(N, X) :-
    ( N > 0 ->
        X = positive
    ;
        X = zero_or_negative
    ).

test2 :-
    p2(0, X),
    show(test2, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 3. then 節の中でさらに if-then-else
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p3(N, X) :-
    ( N >= 0 ->
        ( N =:= 0 ->
            X = zero
        ;
            X = pos
        )
    ;
        X = neg
    ).

test3 :-
    p3(0, X),
    show(test3, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 4. else 節の中でさらに if-then-else
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p4(N, X) :-
    ( N > 10 ->
        X = big
    ;
        ( N > 0 ->
            X = small
        ;
            X = nonpositive
        )
    ).

test4 :-
    p4(3, X),
    show(test4, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 5. 変数共有: 条件で使った変数を then で再利用
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p5(A, B, X) :-
    C is A + B,
    ( C > 10 ->
        X is C * 2
    ;
        X is C - 2
    ).

test5 :-
    p5(4, 3, X),
    show(test5, X).

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 6. 枝ごとに別の変数を導入して最終変数へ流す
