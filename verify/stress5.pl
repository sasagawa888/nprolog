show(Test, X) :-
    write(Test), write(' : '), write(X), nl.

show2(Test, X, Y) :-
    write(Test), write(' : '), write(X), write(','), write(Y), nl.

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 基本: fail して次枝へ
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p1(a) :- fail.
p1(b).

test1 :-
    p1(X),
    show(test1, X).

% expected:
% test1 : b


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 連言の途中失敗 -> 別枝へ
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p2(X) :-
    X = a,
    fail.
p2(X) :-
    X = b.

test2 :-
    p2(X),
    show(test2, X).

% expected:
% test2 : b


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 選言の左枝失敗 -> 右枝成功
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test3 :-
    (fail ; true),
    show(test3, ok).

% expected:
% test3 : ok


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 選言の左枝で一度束縛してから失敗
%% 束縛が右枝へ漏れないか
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test4 :-
    (X = bad, fail ; X = good),
    show(test4, X).

% expected:
% test4 : good


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 連言の最後で失敗 -> 全体失敗 -> 次枝へ
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test5 :-
    ((X = a, Y = b, fail) ; (X = c, Y = d)),
    show2(test5, X, Y).

% expected:
% test5 : c,d


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% if-then-else: 条件失敗 -> else
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test6 :-
    ((fail -> X = then ; X = else)),
    show(test6, X).

% expected:
% test6 : else


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% if-then-else: 条件成功, then側で失敗
%% ISO系なら else へは行かず全体失敗が普通
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test7 :-
    (((true -> fail ; X = else), Y = ng) ; (X = recovered, Y = ok)),
    show2(test7, X, Y).

% expected:
% test7 : recovered,ok


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% ネストした選言 + fail
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test8 :-
    ((fail ; fail ; X = ok)),
    show(test8, X).

% expected:
% test8 : ok


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 失敗を含む複数解
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p9(1).
p9(2) :- fail.
p9(3).

test9 :-
    p9(X),
    show(test9, X),
    fail.
test9.

% expected:
% test9 : 1
% test9 : 3


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 途中失敗で前の束縛が巻き戻るか
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test10 :-
    ((X = 1, fail) ; true),
    (var(X) -> R = unbound ; R = bound),
    show(test10, R).

% expected:
% test10 : unbound
%
% ※ var/1 が無ければこのテストは外してください


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% \+ の内部失敗
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test11 :-
    \+ fail,
    show(test11, ok).

% expected:
% test11 : ok


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% \+ の成功ケース
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

/*
test12 :-
    \+ (X = a),
    show(test12, ok).
