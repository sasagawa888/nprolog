%
% expected:
% test12 : no


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% failを含む補助述語の後で別枝へ
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

bad_path(_) :- fail.
bad_path(_) :- fail.

good_path(ok).

test13 :-
    (bad_path(X) ; good_path(X)),
    show(test13, X).

% expected:
% test13 : ok


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% member風の探索で一部失敗
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p14(a).
p14(b).
p14(c).

q14(a) :- fail.
q14(b).
q14(c) :- fail.

test14 :-
    p14(X),
    q14(X),
    show(test14, X),
    fail.
test14.

% expected:
% test14 : b


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 再帰で途中失敗しても上位で継続可能か
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

p15(0, zero).
p15(1, one).
p15(2, two) :- fail.
p15(3, three).

test15 :-
    p15(_, X),
    show(test15, X),
    fail.
test15.

% expected:
% test15 : zero
% test15 : one
% test15 : three


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 連言内の後半失敗で前半が巻き戻るか
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test16 :-
    ((X = a, Y = 1, fail) ; (X = b, Y = 2)),
    show2(test16, X, Y).

% expected:
% test16 : b,2


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% 二重ネスト if-then-else + fail
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

test17 :-
    (
        fail -> X = outer_then
    ;
        (fail -> X = inner_then ; X = inner_else)
    ),
    show(test17, X).

% expected:
% test17 : inner_else


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%% fail を明示的に回復
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

recover(ok) :- fail.
recover(ok).

test18 :-
    recover(X),
    show(test18, X).

% expected:
% test18 : ok


