% 目的:
%  - fail後に正しく次の選択肢へ進むか
%  - 失敗した枝の変数束縛が残らないか
%  - disjunction, if-then-else, negation, repeat/fail 的な挙動
%  - 途中失敗を含む連言の巻き戻し
%
% 実行:
% ?- run_all.

